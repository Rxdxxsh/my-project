n1=int(input("enter the first number"))
o=input("enter the operator")
n2=int(input("enter the second number"))
if o=="+":
    tot=n1+n2
    print(n1," + ",n2," = ", tot)
elif o=="-":
    tot=n1-n2
    print(n1," - ",n2," = ", tot)
elif o=="/":
    tot=n1/n2
    print(n1," / ",n2," = ", tot)
elif o=="*":
    tot=n1*n2
    print(n1," * ",n2," = ", tot)


